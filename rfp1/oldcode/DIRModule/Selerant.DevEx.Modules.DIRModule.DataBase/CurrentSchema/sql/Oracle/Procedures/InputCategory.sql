CREATE OR REPLACE PACKAGE DXDIR_PK_INPUT_CATEGORY AS

	PROCEDURE InsertRecord
	(
		dcID NUMBER,
		sASSESSMENT_CODE VARCHAR2,
		sTITLE NVARCHAR2,
		sTYPE VARCHAR2
	);

	PROCEDURE UpdateRecord
	(
		dcID NUMBER,
		sASSESSMENT_CODE VARCHAR2,
		sTITLE NVARCHAR2,
		sTYPE VARCHAR2
	);

	PROCEDURE DeleteRecord
	(
		dcID NUMBER
	);

	PROCEDURE GetNextId
	(
        dcNextID OUT NUMBER
    );

END;
/

CREATE OR REPLACE PACKAGE BODY DXDIR_PK_INPUT_CATEGORY AS

	PROCEDURE InsertRecord
	(
		dcID NUMBER,
		sASSESSMENT_CODE VARCHAR2,
		sTITLE NVARCHAR2,
		sTYPE VARCHAR2
	)
	IS
	BEGIN
		INSERT INTO DXDIR_INPUT_CATEGORY
		(
			ID,
			ASSESSMENT_CODE,
			TITLE,
			TYPE
		)
		VALUES
		(
			dcID,
			sASSESSMENT_CODE,
			sTITLE,
			sTYPE
		);
	END;

	PROCEDURE UpdateRecord
	(
		dcID NUMBER,
		sASSESSMENT_CODE VARCHAR2,
		sTITLE NVARCHAR2,
		sTYPE VARCHAR2
	)
	IS
	BEGIN
		UPDATE DXDIR_INPUT_CATEGORY
		SET 
			ASSESSMENT_CODE = sASSESSMENT_CODE,
			TITLE = sTITLE,
			TYPE = sTYPE
		WHERE
			ID = dcID;
	END;

	PROCEDURE DeleteRecord
	(
		dcID NUMBER
	)
	IS
	BEGIN
		DELETE FROM DXDIR_INPUT_CATEGORY
		WHERE
			ID = dcID;
	END;

	PROCEDURE GetNextId
	(
        dcNextID OUT NUMBER
    )
	IS
	BEGIN
		SELECT DXDIR_INPUT_CATEGORY_SEQ.NEXTVAL INTO dcNextID FROM DUAL;
    END;

END;

CREATE OR REPLACE PACKAGE DXDIR_PK_LCSTAGE_TMPL AS

	PROCEDURE InsertRecord
	(
		dcID NUMBER,
		sASSESSMENT_TYPE_CODE VARCHAR2,
		sTITLE NVARCHAR2,
		dcSORT_ORDER NUMBER
	);

	PROCEDURE UpdateRecord
	(
		dcID NUMBER,
		sASSESSMENT_TYPE_CODE VARCHAR2,
		sTITLE NVARCHAR2,
		dcSORT_ORDER NUMBER
	);

	PROCEDURE DeleteRecord
	(
		dcID NUMBER
	);

	PROCEDURE GetNextId
	(
        dcNextID OUT NUMBER
    );

END;
/

CREATE OR REPLACE PACKAGE BODY DXDIR_PK_LCSTAGE_TMPL AS

	PROCEDURE InsertRecord
	(
		dcID NUMBER,
		sASSESSMENT_TYPE_CODE VARCHAR2,
		sTITLE NVARCHAR2,
		dcSORT_ORDER NUMBER
	)
	IS
	BEGIN
		INSERT INTO DXDIR_LC_STAGE_TMPL
		(
			ID,
			ASSESSMENT_TYPE_CODE,
			TITLE,
			SORT_ORDER
		)
		VALUES
		(
			dcID,
			sASSESSMENT_TYPE_CODE,
			sTITLE,
			dcSORT_ORDER
		);
	END;

	PROCEDURE UpdateRecord
	(
		dcID NUMBER,
		sASSESSMENT_TYPE_CODE VARCHAR2,
		sTITLE NVARCHAR2,
		dcSORT_ORDER NUMBER
	)
	IS
	BEGIN
		UPDATE DXDIR_LC_STAGE_TMPL
		SET 
			ASSESSMENT_TYPE_CODE = sASSESSMENT_TYPE_CODE,
			TITLE = sTITLE,
			SORT_ORDER = dcSORT_ORDER
		WHERE
			ID = dcID;
	END;

	PROCEDURE DeleteRecord
	(
		dcID NUMBER
	)
	IS
	BEGIN
		DELETE FROM DXDIR_LC_STAGE_TMPL
		WHERE
			ID = dcID;
	END;

	PROCEDURE GetNextId
	(
        dcNextID OUT NUMBER
    )
	IS
	BEGIN
		SELECT DXDIR_LC_STAGE_TMPL_SEQ.NEXTVAL INTO dcNextID FROM DUAL;
	END;

END;
/

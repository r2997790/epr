CREATE OR REPLACE PACKAGE DXDIR_PK_RESULT_ROW AS

	PROCEDURE InsertRecord
	(
		dcID NUMBER,
		sTITLE VARCHAR2,
		sRESULT_UOM VARCHAR2,
		sRESULT_TYPE VARCHAR2,
		dcSORT_ORDER NUMBER
	);

	PROCEDURE UpdateRecord
	(
		dcID NUMBER,
		sTITLE VARCHAR2,
		sRESULT_UOM VARCHAR2,
		sRESULT_TYPE VARCHAR2,
		dcSORT_ORDER NUMBER
	);

	PROCEDURE DeleteRecord
	(
		dcID NUMBER
	);

END;
/

CREATE OR REPLACE PACKAGE BODY DXDIR_PK_RESULT_ROW AS

	PROCEDURE InsertRecord
	(
		dcID NUMBER,
		sTITLE VARCHAR2,
		sRESULT_UOM VARCHAR2,
		sRESULT_TYPE VARCHAR2,
		dcSORT_ORDER NUMBER
	)
	IS
	BEGIN
		INSERT INTO DXDIR_RESULT_ROW
		(
			ID,
			TITLE,
			RESULT_UOM,
			RESULT_TYPE,
			SORT_ORDER
		)
		VALUES
		(
			dcID,
			sTITLE,
			sRESULT_UOM,
			sRESULT_TYPE,
			dcSORT_ORDER
		);
	END;

	PROCEDURE UpdateRecord
	(
		dcID NUMBER,
		sTITLE VARCHAR2,
		sRESULT_UOM VARCHAR2,
		sRESULT_TYPE VARCHAR2,
		dcSORT_ORDER NUMBER
	)
	IS
	BEGIN
		UPDATE DXDIR_RESULT_ROW
		SET 
			TITLE = sTITLE,
			RESULT_UOM = sRESULT_UOM,
			RESULT_TYPE = sRESULT_TYPE,
			SORT_ORDER = dcSORT_ORDER
		WHERE
			ID = dcID;
	END;

	PROCEDURE DeleteRecord
	(
		dcID NUMBER
	)
	IS
	BEGIN
		DELETE FROM DXDIR_RESULT_ROW
		WHERE
			ID = dcID;
	END;

END;
/
